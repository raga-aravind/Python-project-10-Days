# for loop syntax
for a in "apple":
    print(a)

# For loop using range
for n in range(5):
    print(n)

# for loop using range
for r in range(1,5):
    print(r)

# multiplication table for 2
for i in range(1,11):
    print(f"2 * {i}= {2*i}")

# multiplication table for 3
for i in range(1,11):
    print(f"3 * {i}= {3*i}")